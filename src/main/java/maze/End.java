package maze;

public class End {
    private int endX;
    private int endY;

    // End contructor
    public End(int x, int y) {
        this.endX = x;
        this.endY = y;
    }

    // Get the end x coordinate
    public int getEndX() {
        return endX;
    }

    // Get the end y coordinate
    public int getEndY() {
        return endY;
    }


}
